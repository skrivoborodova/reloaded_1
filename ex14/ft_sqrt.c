int	ft_sqrt(int nb)
{
	int res;
	int i;
	int flag;

	res = 0;
	i = 0;
	flag = 0;
	while (flag == 0)
	{
		if (i * i == nb)
		{
			res = i;
			flag = 1;
		}
		i++;
	}
	return (res);
}
